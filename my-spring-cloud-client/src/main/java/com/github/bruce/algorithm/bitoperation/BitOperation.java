package com.github.bruce.algorithm.bitoperation;

public class BitOperation {

    public static void main(String[] args) {
        System.out.println(5 & 6);
        System.out.println(5 | 6);
        System.out.println(5 ^ 6);
        System.out.println(5 >> 1);
        System.out.println(5 << 1);
    }
}
